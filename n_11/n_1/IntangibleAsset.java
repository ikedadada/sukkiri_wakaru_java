package n_11.n_1;

public class IntangibleAsset {
    String name;
    int price;

    public IntangibleAsset(String name, int price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return this.name;
    }

    public int getPrice() {
        return this.price;
    }
}
